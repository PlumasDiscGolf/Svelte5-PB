</div>
<!-- svelte-ignore a11y_no_noninteractive_tabindex -->
<div class="mt-16 w-full">
	<h2 class="mx-auto mb-6 text-center text-3xl font-bold">Comprehensive Rules</h2>
	<div class="prose lg:prose-xl mx-auto max-w-[75ch]">
		<p>These rules govern the Plumas Disc Golf Tags League (Open) and the Plumas Disc Golf Ladies Tags League, unless otherwise specified.</p>
		<ol>
			<li>
				<strong>Membership</strong>
				<ol><li>All participants must be current Plumas Disc Golf members.</li></ol>
			</li>
			<li>
				<strong>Bag Tags</strong>
				<ol>
					<li>
						<strong>Initial Distribution:</strong> Tags will be initially assigned based on placement in the Membership Drive event.
					</li>
					<li>
						<strong>Weekly Distribution:</strong> At the start of each regular league event, all participants will turn in their tags. Tags will then be redistributed based on the results of that day's two 9-hole rounds. Lowest scores receive the lowest tag numbers.
					</li>
					<li>
						<strong>Tag Order:</strong> Tags will be numbered sequentially. The exact number of tags in circulation will depend on league participation. (e.g., #1 - #50)
					</li>
					<li>
						<strong>Tag Challenges (Outside of Regular League Events):</strong>
						<ol>
							<li>Players may challenge other players for their tag outside of regular league events.</li>
							<li>The format of the challenge round is to be agreed upon by the players involved.</li>
							<li>Challenges must be scheduled and agreed upon by the participating players. Plumas Disc Golf is not responsible for organizing or mediating these challenges.</li>
							<li>One-on-One Challenge: Any tag holder can challenge another with a lower numbered tag. Lower score gains possession of the lower tag number.</li>
							<li>Group Challenge (3 or more players): The tags are distributed based on the finishing order of the group challenge. The player with the best score receives the lowest available tag number, the next best score receives the next lowest available tag number, and so on.</li>
						</ol>
					</li>
					<li>
						<strong>Tiebreakers:</strong> In the event of a tie in scores, the player with the lower tag number <em>coming into</em> the event will be awarded the lower tag number for the following week.
					</li>
					<li>
						<strong>Attendance Requirement (Top 5 Tag Holders):</strong>
						<ol>
							<li>Players holding tags numbered 1 through 5 must attend at least one league event every two weeks.</li>
							<li>
								<strong>Consequences of Missed Attendance:</strong>
								<ol>
									<li>First Offense: If a player holding one of these top 5 tags misses two consecutive league events, they forfeit their tag. The tag will go back into circulation and be awarded at the next league event.</li>
									<li>Multiple Offenses: Multiple violations of the attendance requirement may result in disqualification from the league and/or review by the Plumas Disc Golf Disciplinary Committee for potential membership revocation.</li>
								</ol>
							</li>
						</ol>
					</li>
					<li>
						<strong>Lost or Damaged Tags:</strong> If a player loses or damages their bag tag, they may purchase a replacement tag for $5. The replacement tag will be the next lowest tag number not currently in circulation.
					</li>
				</ol>
			</li>
			<li>
				<strong>League Play</strong>
				<ol>
					<li><strong>Format:</strong> Two 9-hole rounds of singles stroke play.</li>
					<li>
						<strong>Scoring:</strong>
						<ol>
							<li>UDisc will be used for scoring.</li>
							<li>At least one player in each group must keep score during league rounds.</li>
							<li>All players are expected to be willing to keep score for their group as needed.</li>
						</ol>
					</li>
					<li>
						<strong>Rules:</strong> PDGA rules govern play unless otherwise specified in these league rules.
					</li>
				</ol>
			</li>
			<li>
				<strong>Prizes</strong>
				<ol>
					<li>The bag tags themselves are the sole prizes for league participation and achievement.</li>
				</ol>
			</li>
			<li>
				<strong>Conduct and Etiquette</strong>
				<ol>
					<li>All participants are expected to adhere to the PDGA Code of Conduct and demonstrate good sportsmanship.</li>
				</ol>
			</li>
			<li>
				<strong>Fees</strong>
				<ol>
					<li>The only required fee is the annual Plumas Disc Golf membership fee of $25. There are no additional league fees, except for replacement tags ($5).</li>
				</ol>
			</li>
			<li>
				<strong>Registration</strong>
				<ol>
					<li>To participate in the Plumas Disc Golf Bag Tag Leagues, players must have a UDisc account (free accounts are sufficient) and attend any scheduled league event. Assistance with creating a UDisc account will be available at league events for those who need it.</li>
				</ol>
			</li>
			<li>
				<strong>Rules Disputes</strong>
				<ol>
					<li>Rules disputes will be handled by the League Director, if present. If the League Director is not present, rules disputes will be handled by the highest-ranking Club Board Member present, in the following order: Board President, Board Vice President, Other Board Director.</li>
				</ol>
			</li>
			<li>
				<strong>Disqualification</strong>
				<ol>
					<li>Disciplinary issues and potential disqualifications will be handled by the Plumas Disc Golf Disciplinary Committee.</li>
				</ol>
			</li>
			<li>
				<strong>Ladies League Specific Rules</strong>
				<ol>
					<li>Only female players are eligible to participate in the Plumas Disc Golf Ladies Tags League.</li>
				</ol>
			</li>
			<li>
				<strong>Guest Players</strong>
				<ol>
					<li>
						Guests are welcome to play in league events but are not eligible to compete for tags unless they become a Plumas Disc Golf member <em>before</em> the start of the event.
					</li>
					<li>Guests' scores may or may not be recorded. Either way, they will not be considered for tag distribution.</li>
					<li>
						Players who purchase a membership <em>after</em> a league event will be assigned the next available tag number not already distributed.
					</li>
				</ol>
			</li>
			<li>
				<strong>Cancellation of Events</strong>
				<ol>
					<li>
						If a league event is canceled <em>before</em> play begins, tags will not be collected or redistributed.
					</li>
					<li>
						If a league event is canceled <em>during</em> play, tags will be returned to their previous holders.
					</li>
				</ol>
			</li>
			<li>
				<strong>League Director Responsibilities</strong>
				<ol>
					<li>Acquiring yearly tags before the first league event of the year.</li>
					<li>Collecting tags at the beginning of each weekly event.</li>
					<li>Disbursing tags to players at the end of each weekly event.</li>
					<li>Accepting membership payments</li>
					<li>Managing league events and scoring on UDisc.</li>
					<li>Handling rules disputes during league play.</li>
				</ol>
			</li>
			<li>
				<strong>Agreement to the Rules</strong>
				<ol>
					<li>By participating in any Plumas Disc Golf Bag Tag League event, all players agree to abide by these league rules.</li>
				</ol>
			</li>
		</ol>
	</div>
</div>

